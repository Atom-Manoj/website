      <h3>What is a gut microbiome?</h3>
      <p>
        A biome is a distinct ecosystem characterized by its environment and its inhabitants. Surprisingly, the human gut is also a microbiome containing approximately 100 trillion microorganisms (most of them bacteria, but also viruses, fungi, and protozoa) 
        Some gut microbiota are also found in the small intestine and stomach, but primarily, bacteria are present in large intestines. They float inside or attach to the mucous lining of the inner walls.<br><br>

        The types of gut bacteria that live in the colon are different from the types that live elsewhere. They’re majorly anaerobic bacteria that require a low-oxygen environment to survive. If these bacteria stray beyond the colon, they can be harmful. Colon bacteria that manage to creep up and settle in the small intestine can interfere with digestive processes there. Colon bacteria that invade the colon wall or that escape through a wound in the colon wall can cause an infection in our bodies. 
        Although the higher oxygen, faster movement, and strong digestive enzymes in the upper GI tract prevent them from colonizing there. <br><br>

        Anaerobic gut bacteria help break down indigestible fibers in the digestive tract and produce essential nutrients that we can’t get otherwise.The gut microbiome interacts with many of our body systems and assists with many body functions. It plays such an active role in your body that some healthcare service providers have described it as being almost like an organ itself.  <br><br>
        One of the functions is the production of short-chain fatty acids such as acetate, propionate and butyrate. Acetate helps grow other bacteria and is also used in cholesterol synthesis and lipogenesis. Gut microbial enzymes regulate bile metabolism.  <br><br>
        They also help to protect us from unwanted invaders,interacting with the immune system and influencing the extent of inflammation in our guts and elsewhere in our bodies.  <br><br>
        So, we understand that gut microbiota is essential enough to be paid attention to. What are the factors that influence this unique microbiome? 
      </p>
      <h4>Diet</h4>
      <p>
        A range of plant fibers is necessary for the variety of microorganisms in your gut microbiome to flourish. Different creatures prefer different complete foods. They then generate short-chain fatty acids and other byproducts that help reduce the pH in your gut and support the more favorable microorganisms. <br><br>
        However, a diet heavy in sugar and saturated fats tends to encourage bacteria that aren’t as beneficial. Processed foods tend to be high in chemicals and preservatives, which can be detrimental to your microbiome, in addition to being low in fiber and micronutrients (vitamins and minerals). 
      </p>
      <h4>Chemicals</h4>
      <p>
        Pollutants, alcohol, and tobacco smoke are some examples of environmental contaminants that can damage our microbiota. Furthermore, medicines and pesticides have the ability to eradicate both healthy and harmful microorganisms. Certain medications, such as acid blockers, might alter the pH inside your body, which can impact the microbiota.Temporary chemical exposure, such as a brief prescription for a drug that is required to get well, usually doesn’t harm the gut microbiota. On the other hand, prolonged exposure may change its makeup. Certain microorganisms may not be able to flourish when one regularly uses substances like alcohol or takes certain medications. 
      </p>
      <h4>Motility</h4>
      <p>The regular movement within the bowels is known as motility.This is what helps microbes to grow.Many pass through your intestines and help convert substances that are not digested into nutrients that you can absorb. For what duration does this impact your microbiota? Different microorganisms are dispersed throughout the gastrointestinal tract in various locations due to the passage of food and waste through it. If it moves too quickly, they won’t have enough time to settle in or do their work before emptying out. However, if it moves too slowly, they may overgrow and overeat, expanding beyond their original range.<br><br>
        Now that we know the causes, what are the consequences?     
      </p>
      <h3>What is dysbiosis?</h3>
      <p>
        Doctors use the term “dysbiosis” to refer to an unbalanced or unhealthy gut microbiome. <br>
        Dysbiosis can mean: <br>
        <ol>
          <li>A loss or deficit of beneficial bacteria. </li>
          <li>Overgrowth of potentially pathogenic (harmful) bacteria. </li>
          <li>Loss of overall bacterial diversity.</li>
        </ol><br>
        Dysbiosis may start with any of the above, but other symptoms soon follow. 
        A loss of beneficial bacteria leaves your gut vulnerable to more disease-causing or invasive types. These types can overrun the other microorganisms living there, diminishing the overall diversity in your microbiome. 
      </p>
      <h4>Obesity:</h4>
      <p>
        Many studies have shown that obese people show low diversity dysbiosis, i.e., certain bacteria that reduce fat visceral deposits are absent in them. 
        Others include irritable bowel disease, psoriatic arthritis, coeliac disease, etc. <br><br>
        It can be concluded that gut health majorly depends on what we eat or drink and our lifestyle. So, what changes can be made to improve our gut health and, in turn, reduce health risks? <br>
        A study published in Nature Medicine states that specific gut microbes were associated with particular nutrients, foods, food groups, and overall diet composition. Health conditions such as heart disease, type 2 diabetes, obesity, and general inflammation appeared to be most impacted by diet-influenced changes to the microbiome. <br>
        The study shows that eating more unprocessed plant foods such as fruits, vegetables, seeds, nuts, and whole grains results in a thriving gut microbiome. Some animal food, such as eggs and fish, is also beneficial. <br>
        Avoiding animal products such as red meat or bacon, dairy products, and highly processed foods(even plant-based foods like sauces, canned vegetables, and packed juices) helps prevent the colonization of less-healthy gut species. <br>
        Therefore, changing dietary patterns to vegan or ovo-vegetarian(vegetarian +eggs) or pescatarian (fish) benefits your gut health. <br>
        Some studies also suggest that exercising regularly, such as cycling or running, increases good gut bacteria that produce short-chain fatty acids. The results were seen within two weeks. <br>
        These are some changes that can be done on an individual level, but it must also be noted that gut microbiota is affected by genes, environment, and geographical location. <br>
        The microbiota can also be modified by adding live microorganisms to food or by periods of fasting.
      </p>
      <h4>Probiotics:</h4>
      <p>
        These are living bacteria introduced in the host organism in viable form and adequate amounts. They are usually added to yogurts or food supplements.
      </p>
      <h3>Conclusion:</h3>
      <p>
        In conclusion, the gut microbiome is a sensitive and unique biome that affects our overall health. Taking care of this microbiota will help prevent diseases and reduce health risks. 
      </p>
      
