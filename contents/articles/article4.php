    <h1 class="article-title">Treating Cancer with viruses from her own lab</h1>
    <div class="article-meta">
      <span class="author">By Nisha Rathi</span>
    </div>
    <article class="article-content">
        <img src="/images/articles/4a.png" alt="#" class="article-image" />
      <p>
        Recently, it has been noticed that a scientist has successfully treated her own breast cancer by injecting tumours with lab-grown viruses.<br><br>
        Beata Halassy, a virologist at the University of Zagreb, discovered in 2020, at age 49, that she had breast cancer at the site of a previous mastectomy. It was the second occurrence there since her left breast had been removed, and she could not bear to face another bout of chemotherapy. <br><br>
        Halassy decided to take matters into her own hands. Although the treatment was unproven, she decided to try it on herself.<br><br>
        She experimented with oncolytic virotherapy (OVT) to treat her own stage 3 cancer, and she has been cancer-free for four years now.<br><br>
      </p>
      <h3>Emerging field of cancer treatment</h3>
      <p>
        OTV is an up-and-coming emerging field of cancer treatment that uses virus cells to destroy cancerous cells and provokes the immune system into fighting them. Most OTV clinical trials are conducted on late-stage cancer, but now it is also for earlier-stage cancer. The only approved OTV treatment in the US is used to treat metastatic melanoma and is called T-VEC. There is no approved OTV treatment for breast cancer.<br><br>
        Halassy is not a specialist in OTV, but her experience in cultivating viruses gave her the confidence to try the treatment. She targets tumours with two different sets of virus - a measles virus followed by a vesicular stomatitis virus (VSV). Both pathogens are known to infect the type of cell from which the tumour originated, and have already been used in OTV clinical trials. A measles virus has been trialled against metastatic breast cancer.<br><br>
        <i>Diagram showing measles virus</i>
      </p>
      <img src="/images/articles/4b.png" alt="#" class="article-image" />
      <p>
        Halassy has previous experience working with both viruses, and both have good safety records. The strain of measles is extensively used in childhood vaccines, and at worst, it induces mild influenza-like symptoms.<br><br>
        Over a two-month period, a colleague administered a number of treatments with research-grade material freshly prepared by Halassy, which were injected directly into her tumour. Her oncologist agreed to monitor her during self-treatment, so that she will be able to switch to conventional chemotherapy if needed.<br><br>
        This treatment did not show any side effects, and the tumour shrank, effectively becoming softer. It also detached from the pectoral muscle and skin that it had been invading, making it easy to remove surgically.<br><br>
        Analysis of the tumour cell shows that it was covered with immune cells called lymphocytes, suggesting that OTV has worked as expected, provoking the immune system to attack both the virus and tumour cells. After surgery, she received a year-long treatment with an anticancer drug called trastuzumab.<br><br>
        Stephen Russell, an OTV specialist who runs a virotherapy biotech company, Vyraid in Rochester, Minnesota, agrees that Halassy's case suggests viral injections shrink her tumour, but he does not think her experience really breaks any new ground, because researchers are already working on OTV to treat early-stage cancer.
      </p>
      <h3>The Moral Maze</h3>
      <p>
        Halassy felt that it was her responsibility to publish her findings. But she received more than a dozen rejections from journals - mainly, she says, because the paper co-authored with colleagues involved self-experimentation. “The major concern was always ethical issues’’ says Halassy. She was particularly determined to persevere after she came across a review highlighting the value of self-experimentation.<br><br>
        However, the problem is that publishing her results could encourage others to reject conventional methods and something similar to her. People with cancer are susceptible to trying unproven methods. Yet she notes that knowledge that comes from self-experimentation is never lost. The paper also mentions that it should not be the first approach towards a diagnosis of cancer.<br><br>
        Halassy has no regrets about self-experimentation. She thinks that it is very unlikely that someone will try to copy her, because treatment requires so much scientific knowledge and skill. She has a lot more experience in cultivating viruses. Halassy has obtained funding to investigate OTV to treat cancer in domestic animals, and her focus has completely changed after a positive experience of self-experimentation.
      </p>
      
