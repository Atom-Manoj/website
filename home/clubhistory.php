<section class="origin-section">
  <div class="container">
    <h2>Origin & Evolution</h2>
    <p>
      JIGYANSA was conceived after U Abinash Patro attended a LIGO science communication workshop. Inspired by efforts in Marathi, he initiated a discussion among peers to create a similar initiative in Odia.<br /><br />
      Together with Gyanaranjan Parida, the idea was proposed in an Odia community group at IISER Berhampur and received overwhelming support. With backing from peers in clubs like Naxatra, it was officially launched on Utkala Divas, 2021.<br /><br />
      The initial vision was to make science accessible to high school and intermediate students lacking native-language resources.
    </p>
